package com.example.apptesttecnic;

import androidx.lifecycle.ViewModel;

//Este viewModel sirve para guardar los datos necesarios en DetailArtist
public class DetailArtistViewModel extends ViewModel {
    private int id;
    private String first_name,last_name,description,image;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
